package com.negativespace.ld34;

public class Frame {
	
	public int x, y, width, height, offsetX, offsetY;
	
	public Frame(int x, int y, int width, int height, int offsetX, int offsetY){
		this.x=x;
		this.y=y;
		this.width=width;
		this.height=height;
		this.offsetX=offsetX;
		this.offsetY=offsetY;
	}

}
